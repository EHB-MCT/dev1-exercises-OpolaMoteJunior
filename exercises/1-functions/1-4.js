"use strict";

let canvas = document.querySelector('canvas');
canvas.width = window.innerWidth;
canvas.height = window.innerHeight;
let context = canvas.getContext('2d')

function drawSquare(); {
    context.beginPath();
    context.moveTo(50,50);
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.lineTo();
    context.stroke();
}
 
